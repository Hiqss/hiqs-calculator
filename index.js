var numberOne = parseInt(prompt ("Enter first number"))
var numberTwo = parseInt(prompt ("Enter second number"))
var Operator = prompt ("Enter Operator you want to perform(+, -, *, /)")

  if (Operator == "+")
   {
    alert (numberOne + numberTwo)
  }
  else if (Operator == "-") 
  {
    alert (numberOne - numberTwo)
  }
  else if (Operator == "/")
  {
    alert (numberOne / numberTwo)
  }
  else if (Operator == "*") 
  {
    alert (numberOne * numberTwo)
  }